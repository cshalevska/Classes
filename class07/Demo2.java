public class Demo2 {

	public static void main(String[] args) {
		// I need to print value from 2 to 20
		
		int a=2;
		
		while (a<=20) {
			System.out.println(a);
			a++;
		}
		
		// print 1- 50 all in one line 
		
		int b=1;
		
		while(b<=50) {
			System.out.print(b+" " );
			b++;
		}
		System.out.println("--------------------");
		System.out.println(" I need Numbers from 20 to 1");
	
	
		int  c=20;
		
		while (c>=1) {
			System.out.print(c+ " ");
			c--;
		}
			
	
		System.out.println("The end of the code");

}
}

